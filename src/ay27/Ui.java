    @Override
    public void setMsgViewerText(final List<Message> msgs) {
        Document doc = msgViewer.getStyledDocument();
        try {
            doc.remove(0, doc.getLength());
        } catch (BadLocationException e) {
            e.printStackTrace();
        }

        SimpleAttributeSet metas = new SimpleAttributeSet();
        StyleConstants.setForeground(metas, Color.GRAY);
        StyleConstants.setBold(metas, true);
        StyleConstants.setFontSize(metas, 12);

        SimpleAttributeSet msgAttr = new SimpleAttributeSet();
        StyleConstants.setFontSize(msgAttr, 16);

        SimpleDateFormat df = new SimpleDateFormat("MM-dd HH:mm:ss");

        for (Message msg : msgs) {
            try {
                if (msg.isMySelf()) {
                    doc.insertString(doc.getLength(), "我: " + df.format(msg.getDate()), metas);
                } else {
                    doc.insertString(doc.getLength(), msg.getUserName() + ": " + df.format(msg.getDate()), metas);
                }
                doc.insertString(doc.getLength(), "\n" + msg.getContent() + "\n\n", msgAttr);
            } catch (BadLocationException e) {
                e.printStackTrace();
            }
        }
    }

