    @Override
    public File recvFile(String userName, String fileName) {
        int accpet = JOptionPane.showConfirmDialog(null, "是否接收来自 " + userName + " 的文件: " + fileName + " ?", "文件接收", JOptionPane.YES_NO_OPTION);
        if (accpet == 1) {
            System.out.println("accept = "+accpet);
            return null;
        }
        JFileChooser jFileChooser = new JFileChooser("选择保存的位置");
        jFileChooser.showSaveDialog(null);
        return jFileChooser.getSelectedFile();
    }


